// 1. Arithmetic Operators

console.log(2 + 2); // Use + * - /

// 2. Assignment Operators

let total = 2 + 2;
console.log(total);
total += 1; // total = total + 1;
console.log(total);
total -= 3; // total = total - 3;
console.log(total);

// 3. Comparison Operators

console.log(2 == "2");
console.log(2 === "2");
console.log(2 === 2);

console.log(1 !== 2);
console.log(1 > 2);
console.log(1 < 2);
console.log(10 >= 10);
console.log(11 >= 10);
console.log(10 <= 10);
console.log(9 <= 10);

// 4. Logical Operators

console.log(1 + 1 == 1 || 1 + 1 == 2); // change to &&

const isActive = true; // change value
console.log(!isActive);
